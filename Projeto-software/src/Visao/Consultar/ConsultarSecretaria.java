    public void AtualizaTable() {
        
        Connection con = Conexao.AbrirConexao();
        SecretarioDAO bd = new SecretarioDAO(con);
        List<Secretaria> lista = new ArrayList<>();
        lista = bd.ListarSecretaria();
        
        DefaultTableModel tbm = (DefaultTableModel) jTable1.getModel();
        
        while (tbm.getRowCount() > 0) {
            
            tbm.removeRow(0);
            
        }

        int i = 0;
        for (Secretaria tab : lista) {
            
            tbm.addRow(new String[i]);
            jTable1.setValueAt(tab.getId(), i, 0);
            jTable1.setValueAt(tab.getLogin(), i, 1);
            jTable1.setValueAt(tab.getNome(), i, 2);
            jTable1.setValueAt(tab.getEndereco(), i, 3);
            jTable1.setValueAt(tab.getCpf(), i, 4);
            jTable1.setValueAt(tab.getRg(), i, 5);
            jTable1.setValueAt(tab.getTelefone(), i, 6);

            i++;
            
        }
        
        Conexao.FecharConexao(con);
        
    }
    
